        const clause = (pick.clause || stripName(m.id, claim.statement).split(/[;.]/)[0].slice(0, 120)).replace(/[\s.;,]+$/, '');
        if (SKIP.test(clause)) continue;   // proof doesn't bear the connection → drop, never assert
