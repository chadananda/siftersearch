// Live grounding state is reported by the executor (api/lib/pipeline/run-grounding.js) INTO doc_pipeline.run_json —
// the single source of truth (replaces the old data/siftersearch-grounding-status.json file). bio.js READS it; it
// never infers the active book from a file. `pipelineState.activeRun()` returns the freshest non-idle run, or null.
// The stages, in order — must match run-grounding.js GROUNDING_STAGES (drives the current book's % progress).
const GROUNDING_STAGES = ['disambiguate', 'mentions', 'claims', 'reconcile', 'research', 'project', 'link', 'merge', 'dedup', 'hype', 'verify'];
// Per-DOC cache (books ground in parallel, so a single-slot cache would thrash between them each poll).
const _activeCache = new Map();   // docId → { sig, at, value }

// Per-book stage job sizes (item counts) → the bar's per-stage weights. prose paras drive the paragraph passes
// (disambiguate/claims/hype); distinct mention-clusters drive reconcile (the big one on biography volumes);
// research ≈ a stable fraction of clusters (NOT the live 'uncertain' count, which grows during reconcile and
// would wobble the total). The quick bookkeeping stages get a small nominal so they contribute a sliver, never 0.
// Static per run → cached ~2min by doc (a GROUP BY over mentions is too heavy to run on every 3s poll).
const _sizeCache = new Map();
const _pctFloor = new Map();   // docId → {startedAt, pct}: per-run monotonic floor so emitted % never regresses
async function stageSizes(docId) {
  const hit = _sizeCache.get(docId);
  if (hit && Date.now() - hit.at < 120000) return hit.sizes;
  const prose = (await queryAll(`SELECT COUNT(*) n FROM content WHERE doc_id=? AND blocktype IN ('paragraph','quote') AND deleted_at IS NULL`, [docId]))[0]?.n || 0;
  const clusters = (await queryAll(`SELECT COUNT(DISTINCT resolved_as) n FROM entity_mentions_v2 WHERE doc_id=? AND resolved_as IS NOT NULL`, [docId]))[0]?.n || 0;
  const sizes = {
    disambiguate: prose, mentions: prose ? 30 : 0, claims: prose,
    reconcile: clusters, research: Math.round(clusters * 0.3),
    project: 30, link: 30, merge: 60, dedup: 30, hype: prose, verify: 20,
  };
  _sizeCache.set(docId, { sizes, at: Date.now() });
  return sizes;
}

/**
 * The live block for ONE grounding book. Takes the run (rather than fetching "the" run) so N books in flight
 * each get their own block — the API reports a LIST and the UI tabs between them.
 * `run` may be null: then the claim-write fallback below infers a book (legacy/transition runs).
 */
async function computeActiveBook(run, staticDocs, meta) {
  let docId = run?.doc_id ?? null, stage = run?.stage ?? null;
  let si = run?.stageIndex ?? null;
  const ts = run?.totalStages ?? GROUNDING_STAGES.length;
  const startedAt = run?.startedAt ?? null;
  // Lightweight: the active block is polled ~30s. Cache ~20s per doc, keyed by the run signature, so repeated
  // polls (and multiple viewers) don't re-run COUNTs against the live DB while grounding writes.
  const sig = run ? `${docId}:${stage}:${run.updatedAt}` : 'none';
  const hit = _activeCache.get(docId ?? 'none');
  if (hit && hit.sig === sig && Date.now() - hit.at < 20000) return hit.value;
  const remember = (value) => { _activeCache.set(docId ?? 'none', { sig, at: Date.now(), value }); return value; };
  // Fallback (transition / a run predating run_json): the curated book with the most RECENT claim write — but ONLY if
  // that write is <5min old (grounding genuinely live). Stale writes = nothing active, so idle shows no book.
  if (!docId && staticDocs.length) {
    const ph = staticDocs.map(() => '?').join(',');
    const row = (await queryAll(`SELECT doc_id d, MAX(asserted_at) m FROM entity_claims
        WHERE doc_id IN (${ph}) GROUP BY doc_id ORDER BY m DESC LIMIT 1`, staticDocs))[0];
    if (row?.d && row.m && Date.now() - new Date(row.m).getTime() < 5 * 60000) { docId = row.d; stage = 'grounding'; }
  }
  if (!docId) return remember(null);
  const claims = (await queryAll(`SELECT COUNT(*) n FROM entity_claims WHERE doc_id=?`, [docId]))[0]?.n || 0;
  // No live status file (older driver / prefetch): INFER how far the book has progressed from its DB artifacts
  // so the current book still shows a real progress bar — disambiguated → mentions → claims → bound (reconciled)
  // → HyPE. This is the "how far along" signal the popup exists to surface.
  if (si == null) {
    const one = async (sql) => (await queryAll(sql, [docId]))[0]?.n || 0;
    const bound = await one(`SELECT COUNT(*) n FROM entity_mentions_v2 WHERE doc_id=? AND entity_id IS NOT NULL`);
    const hype = await one(`SELECT COUNT(*) n FROM content WHERE doc_id=? AND hyp_questions IS NOT NULL`);
    const ment = await one(`SELECT COUNT(*) n FROM entity_mentions_v2 WHERE doc_id=?`);
    const at = (name) => GROUNDING_STAGES.indexOf(name);
    si = (hype > 0 && bound > 0) ? at('hype') : bound > 0 ? at('link') : claims > 0 ? at('claims') : ment > 0 ? at('mentions') : 0;
    stage = GROUNDING_STAGES[si];
  }
  // Within-stage progress so the bar CLIMBS during a long stage (not frozen at the stage boundary). The claims
  // stage dominates wall-time, so measure it directly: distinct paragraphs with claims / disambiguated paragraphs.
  // Other stages get a 0.5 mid-estimate (they're short). percent = (stageIndex + withinFrac) / totalStages.
  const stageName = stage || GROUNDING_STAGES[si] || 'grounding';
  let withinFrac = 0.5;
  // PREFERRED: the executor reports REAL per-item progress (paras/clusters/uncertains settled ÷ job size) into
  // run_json.withinFrac — every long stage flows through pool()'s onProgress. Use it directly (no DB counts) so the
  // bar climbs continuously through EVERY stage; a flat bar then means work actually stopped, not "unobserved".
  if (run && typeof run.withinFrac === 'number') {
    withinFrac = Math.max(0, Math.min(0.999, run.withinFrac));
  } else if (stageName === 'disambiguate') {
    // Disambiguate is a full model pass over every prose paragraph — measure it directly (context-filled / total prose)
    // so the bar climbs through it instead of sitting flat.
    const totalPar = (await queryAll(`SELECT COUNT(*) n FROM content WHERE doc_id=? AND blocktype IN ('paragraph','quote') AND deleted_at IS NULL`, [docId]))[0]?.n || 0;
    const disPar = (await queryAll(`SELECT COUNT(*) n FROM content WHERE doc_id=? AND context IS NOT NULL`, [docId]))[0]?.n || 0;
    withinFrac = totalPar ? Math.min(0.99, disPar / totalPar) : 0.5;
  } else if (stageName === 'claims') {
    const totalPar = (await queryAll(`SELECT COUNT(*) n FROM content WHERE doc_id=? AND context IS NOT NULL`, [docId]))[0]?.n || 0;
    const claimedPar = (await queryAll(`SELECT COUNT(DISTINCT para_id) n FROM entity_claims WHERE doc_id=?`, [docId]))[0]?.n || 0;
    withinFrac = totalPar ? Math.min(0.99, claimedPar / totalPar) : 0.5;
  } else if (stageName === 'hype') {
    // HyPE is the other long stage on big books — measure it directly (paragraphs with questions / disambiguated).
    const totalPar = (await queryAll(`SELECT COUNT(*) n FROM content WHERE doc_id=? AND context IS NOT NULL`, [docId]))[0]?.n || 0;
    const hypePar = (await queryAll(`SELECT COUNT(*) n FROM content WHERE doc_id=? AND hyp_questions IS NOT NULL`, [docId]))[0]?.n || 0;
    withinFrac = totalPar ? Math.min(0.99, hypePar / totalPar) : 0.5;
  }
  // Bar weight per stage = its REAL job size (item count), measured PER BOOK. Fixed weights can't be right for
  // both a narrative (claims/paragraphs dominate) and a biographical volume (reconcile — thousands of clusters —
  // is >half the work). Each item ≈ one model call, so item-count is a good wall-time proxy: the stage that
  // actually takes the time gets the bar-share, so a long reconcile CLIMBS instead of sitting flat at ~68%.
  const sizes = await stageSizes(docId);
  const total = Object.values(sizes).reduce((a, b) => a + b, 0) || 1;
  let done = 0;
  for (let i = 0; i < GROUNDING_STAGES.length; i++) {
    const frac = i < si ? 1 : (i === si ? withinFrac : 0);   // prior stages full, current by within-frac, rest 0
    done += (sizes[GROUNDING_STAGES[i]] / total) * frac;
  }
  // HIGH-RESOLUTION float (2dp) — never the old integer that froze each whole-percent for ~a minute of real work.
  let percent = Math.max(3, Math.min(99, Math.round(done * 10000) / 100));
  // MONOTONIC per run (keyed by startedAt): clamp so the emitted percent can never regress at the source — kills
  // any backward jump from a re-weight / recompute / transient read. A genuinely new run (different startedAt) resets.
  const floor = _pctFloor.get(docId);
  if (floor && floor.startedAt === startedAt) percent = Math.max(percent, floor.pct);
  _pctFloor.set(docId, { startedAt, pct: percent });
  // Current-stage DETAIL (absolute done/total, now that stages report ABSOLUTE progress) — the "window into what's
  // happening" the bar surfaces: e.g. "Reconciling identities · 1,664 / 6,533".
  const stageDone = run?.itemsDone ?? null, stageTotal = run?.itemsTotal ?? null;
  const stageFrac = stageTotal ? Math.min(1, (stageDone || 0) / stageTotal) : withinFrac;
  // Always resolve the real title — the active book may not be in a phase (e.g. a stray-collection doc), so fall
  // back to the docs table rather than showing "doc <id>".
  let m = meta[docId];
  if (!m?.title) m = (await queryAll(`SELECT title, paragraph_count FROM docs WHERE id=?`, [docId]))[0] || m;
  const active = { docId, stage: stageName, stageIndex: si, totalStages: ts, percent, since: startedAt,
    stageDone, stageTotal, stageFrac: Math.round(stageFrac * 1000) / 1000,
    adjudicatorVersion: ADJUDICATOR_VERSION,   // the engine version this run is grounding with (shown in the progress box)
    title: m?.title || `doc ${docId}`, size: m?.paragraph_count || 0, claimsExtracted: claims };
  return remember(active);
}

/**
 * The live block for EVERY grounding book (freshest first). Books run in parallel by design, so this is the
 * list the UI tabs across; `active` (singular, = the first) stays for callers that just want "the current book".
 */
async function computeActiveBooks(staticDocs, meta) {
  const runs = await pipelineState.activeRuns();
  if (!runs.length) {                                   // nothing reported → let the claim-write fallback try
    const one = await computeActiveBook(null, staticDocs, meta);
    return one ? [one] : [];
  }
  const blocks = await Promise.all(runs.map((r) => computeActiveBook(r, staticDocs, meta)));
  return blocks.filter(Boolean);
}

// Integration progress for the biography "progress" popup — the phased roadmap (integration-phases.js): every
// book in sequence with its SIZE (paragraph_count), LIVE grounded-person count, and the currently-active book.
// The phase structure is cached briefly; the `active` block is recomputed on EVERY call so polling stays live.
let _progCache = null, _progAt = 0;
export async function getIntegrationProgress() {
  // genreOf is DISPLAY-ONLY now (the biographies/histories genre labels) — it no longer decides membership.
  const genreOf = Object.fromEntries(readHistoryCatalog().map(b => [b.id, b.genre]));
  // Membership is EXPLICIT: a doc is in the plan IFF its id is listed in integration-phases.js. No author-routing,
  // no collection sweep, no genre auto-classification — nothing rides in on a rule. (Pilgrim-group docs, primary.groups,
  // render as a separate tree below and are intentionally NOT part of phaseByDoc / the graded metrics.)
  const phaseByDoc = {};
  for (const p of INTEGRATION_PHASES) for (const id of (p.docs || [])) phaseByDoc[id] = p.key;
  const allDocs = [...new Set(Object.keys(phaseByDoc).map(Number))];

  // Size + title/author for EVERY book (chunked to stay under the SQLite param limit).
  const meta = {};
  for (let i = 0; i < allDocs.length; i += 800) {
    const chunk = allDocs.slice(i, i + 800), cp = chunk.map(() => '?').join(',');
    (await queryAll(`SELECT id, title, author, paragraph_count FROM docs WHERE id IN (${cp}) AND deleted_at IS NULL AND duplicate_of IS NULL`, chunk))
      .forEach(d => { meta[d.id] = d; });
  }
  const curatedKeys = new Set(INTEGRATION_PHASES.filter(p => !p.dynamic).map(p => p.key));
  const gradedDocs = allDocs.filter(id => curatedKeys.has(phaseByDoc[id])); // curated docs that may be grounded
  // `actives` = every book grounding right now (parallel runs are normal); `active` = the first, kept so existing
  // callers/UI keep working. Always fresh (cheap) → live polling.
  const actives = await computeActiveBooks(gradedDocs, meta);
  const active = actives[0] || null;
  const offPeak = await computeOffPeakHint();   // {waiting, resumesAt} — books held for cheap DeepSeek hours (fresh, cheap)

  if (_progCache && Date.now() - _progAt < 60000) return { ..._progCache, active, actives, offPeak };

  // Grounded metrics run over the curated (non-dynamic) docs in phase order — biographies/histories aren't grounded yet.
  const ph = gradedDocs.map(() => '?').join(',');
  const counts = {}, unresolved = {};
  (await queryAll(`SELECT doc_id, COUNT(*) n FROM (
      SELECT doc_id, entity_id FROM entity_mentions_v2 WHERE doc_id IN (${ph}) AND entity_id IS NOT NULL
      UNION SELECT doc_id, entity_id FROM entity_claims WHERE doc_id IN (${ph}) AND entity_id IS NOT NULL
    ) GROUP BY doc_id`, [...gradedDocs, ...gradedDocs])).forEach(r => { counts[r.doc_id] = r.n; });
  (await queryAll(`SELECT CAST(json_extract(payload,'$.docId') AS INT) d, COUNT(*) n FROM entity_decisions
      WHERE kind='uncertain' AND status='proposed' GROUP BY d`)).forEach(r => { if (r.d) unresolved[r.d] = r.n; });
  // HONEST "done": a book is done only when reconcile has actually adjudicated its cast — decisions exist AND cover
  // most of its name-clusters. Just having ≥1 legacy-bound person is NOT done (the Covenant books had 0 decisions
  // yet showed ✓). decsByDoc counts reconcile decisions; clustersByDoc counts distinct resolvable names.
  const decsByDoc = {}, clustersByDoc = {}, adjVerByDoc = {};
  (await queryAll(`SELECT CAST(json_extract(payload,'$.docId') AS INT) d, COUNT(*) n FROM entity_decisions
      WHERE target_kind='mention-cluster' GROUP BY d`)).forEach(r => { if (r.d) decsByDoc[r.d] = r.n; });
  (await queryAll(`SELECT doc_id d, COUNT(DISTINCT resolved_as) n FROM entity_mentions_v2
      WHERE doc_id IN (${ph}) AND resolved_as IS NOT NULL AND resolved_as NOT LIKE '%?%' GROUP BY doc_id`, gradedDocs))
    .forEach(r => { clustersByDoc[r.d] = r.n; });
  // Adjudicator version = the HIGHEST engine version that has produced decisions for this book — i.e. the version
  // it was LAST processed at. A pre-EEWA decision has NULL method_version → v1 (the original thin reconcile); an
  // EEWA re-sweep writes v2 decisions → MAX = 2. MAX (not MIN) is the point of the badge: "has this book been
  // upgraded to the current engine?" A few stragglers (parse-failures that kept a v1 decision) must NOT drag a
  // swept book back to v1. (No decisions → absent → 0 → hidden.)
  (await queryAll(`SELECT CAST(json_extract(payload,'$.docId') AS INT) d,
      MAX(COALESCE(CAST(method_version AS INT), 1)) v FROM entity_decisions
      WHERE target_kind='mention-cluster' GROUP BY d`)).forEach(r => { if (r.d) adjVerByDoc[r.d] = r.v; });
  // SPEND per book, from the central ai_usage log — what this book actually cost and on whose meter. Only
  // grounding calls (service_type 'grounding:<stage>') count, so chat/search spend never inflates a book.
  // Rolled up per provider because provider IS the story: deepseek is ~free, anthropic is the Persian budget.
  // CAST: document_id is a TEXT column, so numeric ids are stored as text — historically as '15254.0' (a bound
  // JS number under TEXT affinity). Grouping on the raw column would key this map by a string that never matches
  // an integer book id → every book reads $0. Cast on read so both new ('15254') and legacy ('15254.0') rows roll up.
  const costByDoc = {};
  // 6dp, not 2: an individual call costs ~1e-5, so rounding to cents floors a real cost to $0 — under-reporting
  // is the same failure as not reporting. Sum first, round once.
  // Match on the CALLER as well as the stage tag: a grounding call is a corpus-rag call whatever its stage
  // label, so a row logged before the stage tag existed (or from a stage-less path) still counts toward its book.
  (await queryAll(`SELECT CAST(document_id AS INT) d, provider p, ROUND(SUM(estimated_cost_usd), 6) usd, COUNT(*) calls
      FROM ai_usage WHERE (service_type LIKE 'grounding:%' OR caller = 'corpus-rag') AND document_id IS NOT NULL
      GROUP BY d, p`))
    .forEach(({ d, p, usd, calls }) => {
      const c = (costByDoc[d] ||= { usd: 0, calls: 0, byProvider: {} });
      c.usd = Math.round((c.usd + usd) * 1e6) / 1e6; c.calls += calls; c.byProvider[p] = usd;
    });
  // Done ⇔ the book reached FULL grounding, tested by the SAME reachedBound() the queue uses to decide a run
  // finished — one definition of "done", so the roadmap ✓ can never disagree with the pipeline. Reconcile alone
  // is NOT enough: a read-half-only book (disambiguate→reconcile done, but the graph tail + HyPE never ran → 0
  // entity bindings, 0 hyp_questions) has decisions covering its clusters (clusters exist only for processed
  // paragraphs) yet is not grounded. That was the 8322/9095 leak — both showed ✓ on reconcile while fully
  // unprojected + un-HyPE'd. reachedBound(verify) requires disamb≥98% + mentions + claims + reconcile + HyPE.
  const doneByDoc = {};
  await Promise.all(gradedDocs.map(async (id) => { try { doneByDoc[id] = await reachedBound(id, {}); } catch { doneByDoc[id] = false; } }));
  const isGrounded = (id) => doneByDoc[id] === true;
  // Order IS the hard-coded list order (integration-phases.js was snapshotted in the intended grounding sequence).
  // No dynamic re-sort — the list is the order, so the roadmap and the follower that walks it stay stable and match
  // the file exactly. A listed id that's since been soft-deleted/dup'd has no meta → filtered out here.
  const phaseBookIds = {};
  for (const p of INTEGRATION_PHASES) phaseBookIds[p.key] = (p.docs || []).filter((id) => meta[id]);
  // New-in-sequence: the first book (in ABSORPTION order) to ground each entity → each book's NET contribution.
  const orderedGraded = INTEGRATION_PHASES.filter(p => !p.dynamic).flatMap(p => phaseBookIds[p.key]);
  const order = {}; orderedGraded.forEach((d, i) => { order[d] = i; });
  const firstSeen = {}, newBy = {};
  (await queryAll(`SELECT doc_id d, entity_id e FROM entity_mentions_v2 WHERE doc_id IN (${ph}) AND entity_id IS NOT NULL
      UNION SELECT doc_id, entity_id FROM entity_claims WHERE doc_id IN (${ph}) AND entity_id IS NOT NULL`, [...gradedDocs, ...gradedDocs]))
    .forEach(({ d, e }) => { const o = order[d]; if (o !== undefined && (firstSeen[e] === undefined || o < firstSeen[e])) firstSeen[e] = o; });
  for (const e in firstSeen) { const d = orderedGraded[firstSeen[e]]; newBy[d] = (newBy[d] || 0) + 1; }

  const book = (id, extra = {}) => ({ id, title: meta[id]?.title || `doc ${id}`, author: meta[id]?.author || null,
    size: meta[id]?.paragraph_count || 0, persons: counts[id] || 0, newInSequence: newBy[id] || 0,
    unresolved: unresolved[id] || 0, done: isGrounded(id),
    adjVersion: adjVerByDoc[id] ?? 0, adjCurrent: ADJUDICATOR_VERSION,
    cost: costByDoc[id] || { usd: 0, calls: 0, byProvider: {} }, ...extra });
  const phases = INTEGRATION_PHASES.map(p => {
    const books = phaseBookIds[p.key].map(id => book(id, p.dynamic ? { genre: genreOf[id] } : {}));
    return { key: p.key, label: p.label, blurb: p.blurb, upcoming: !!p.upcoming, books,
      done: books.filter(b => b.done).length, total: books.length,
      persons: books.reduce((s, b) => s + b.persons, 0), paras: books.reduce((s, b) => s + b.size, 0),
      usd: Math.round(books.reduce((s, b) => s + b.cost.usd, 0) * 100) / 100 };
  });

  // Pilgrim Notes — the Primary phase's per-period sub-groups, from the EXPLICIT hard-coded group lists
  // (integration-phases.js `primary.groups`), NOT a collection sweep. Resolve title/size + a has-claims "grounded"
  // flag per id; a soft-deleted/duplicate id drops out (meta filter), so the tree only shows live, listed docs.
  const primarySpec = INTEGRATION_PHASES.find((p) => p.key === 'primary');
  if (primarySpec?.groups?.length) {
    const groupIds = [...new Set(primarySpec.groups.flatMap((g) => g.docs || []))];
    const gmeta = {}; const grounded = new Set();
    for (let i = 0; i < groupIds.length; i += 800) {
      const c = groupIds.slice(i, i + 800), cp = c.map(() => '?').join(',');
      (await queryAll(`SELECT id, title, paragraph_count FROM docs WHERE id IN (${cp}) AND deleted_at IS NULL AND duplicate_of IS NULL`, c)).forEach((d) => { gmeta[d.id] = d; });
      (await queryAll(`SELECT DISTINCT doc_id d FROM entity_claims WHERE doc_id IN (${cp})`, c)).forEach((r) => grounded.add(r.d));
    }
    const groups = primarySpec.groups.map((g) => {
      const books = (g.docs || []).filter((id) => gmeta[id]).map((id) => ({ id, title: gmeta[id].title, size: gmeta[id].paragraph_count || 0, done: grounded.has(id) }));
      return { label: g.label, books, total: books.length, done: books.filter((b) => b.done).length, paras: books.reduce((s, b) => s + b.size, 0) };
    });
    const primary = phases.find((p) => p.key === 'primary');
    if (primary) {
      primary.groups = groups;
      primary.total += groups.reduce((s, g) => s + g.total, 0);
      primary.done += groups.reduce((s, g) => s + g.done, 0);
      primary.paras += groups.reduce((s, g) => s + g.paras, 0);
    }
  }
  const doneBooks = phases.reduce((s, p) => s + (p.upcoming ? 0 : p.done), 0);
  const totalBooks = phases.reduce((s, p) => s + p.total, 0);
  // Cumulative UNIQUE people grounded across the curated books (deduped — a figure in N books counts once).
  const cumulativeUnique = (await queryAll(`SELECT COUNT(*) n FROM (
      SELECT entity_id e FROM entity_mentions_v2 WHERE doc_id IN (${ph}) AND entity_id IS NOT NULL
      UNION SELECT entity_id FROM entity_claims WHERE doc_id IN (${ph}) AND entity_id IS NOT NULL)`, [...gradedDocs, ...gradedDocs]))[0]?.n || 0;
  const totalParas = phases.reduce((s, p) => s + p.paras, 0);
  _progCache = { phases, doneBooks, totalBooks, cumulativeUnique, totalParas, goal: 'all history absorbed' };
  _progAt = Date.now();
  return { ..._progCache, active, actives, offPeak };
}

// Off-peak hold hint for the progress UI: is DeepSeek work QUEUED but held for the cheap window? If so, when does
// it resume? Non-sensitive (no dollar figures) so it rides the public /people/progress endpoint. Fresh each poll.
async function computeOffPeakHint() {
  try {
    const rows = await queryAll(`SELECT peak_windows FROM grounding_budget WHERE offpeak_only=1`);
    if (!rows.length) return { waiting: false, resumesAt: null };
    const now = new Date();
    let resumesAt = null;
    for (const r of rows) {
      let w = DEFAULT_PEAK_WINDOWS; try { if (r.peak_windows) w = JSON.parse(r.peak_windows); } catch { /* default */ }
      if (!nowInPeak(w, now)) continue;
      const e = peakEndsAt(w, now); if (!e) continue;
      const ts = Math.floor(e.getTime() / 1000);
      resumesAt = resumesAt == null ? ts : Math.min(resumesAt, ts);
    }
    if (resumesAt == null) return { waiting: false, resumesAt: null };
    const q = await queryOne(`SELECT COUNT(*) n FROM grounding_queue WHERE status='queued'`);
    return { waiting: (q?.n || 0) > 0, resumesAt: (q?.n || 0) > 0 ? resumesAt : null };
  } catch { return { waiting: false, resumesAt: null }; }  // tables may not exist (tests/fresh db)
}

